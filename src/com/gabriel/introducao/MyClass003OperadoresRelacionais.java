package com.gabriel.introducao;

public class MyClass003OperadoresRelacionais {
    public static void main(String[] args) {

        // usados para comparar dois valores
        // SEMPRE retornam um valor boolean (true or false)
        // < >  <= >= == !=
        // menor, maior, menor ou igual, maior ou igual, igual, diferente

        System.out.println( 10 > 20); // false
        System.out.println(10 != 4); // true
        System.out.println( 30 == 30); // true
        System.out.println(53 <= 32); // false
        System.out.println(21 >= 12); // true

        // Lembre-se: Comparação de tipo composto (Objeto), não se usa esses operadores

    }
}
