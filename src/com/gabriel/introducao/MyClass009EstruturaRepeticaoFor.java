package com.gabriel.introducao;

public class MyClass009EstruturaRepeticaoFor {
    public static void main(String[] args) {

        // Tem o mesmo objetivo do While, mas possui algumas particularidades
        // Basicamente cada estrutura de repetição de adequa melhor a uma determinada situação

        // Ideal quando você sabe de antemão quantas vezes o bloco de código deve ser executado


//      Sintaxe: (inicializa variável local; condição; incremento)

        for(int i = 0; i < 10; i++) { // contar de 0 - 9
            System.out.println(i);
        }

        // for-each: Para iterar sobre elementos de arrays e coleções de maneira simplificada
        // Estudo sobre o assunto mais a frente

    }
}
